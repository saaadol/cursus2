#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*temp;
	int		i;

	i = 0;
	temp = (char *)malloc(sizeof (char) * (ft_strlen(s1) + 1));
	if (!temp)
		return (0);
	while (s1[i])
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
